const mongoose = require("mongoose");

const EventSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    date: {
      type: Date,
      required: true,
    },
    location: {
      type: String,
      required: true,
    },
    image: {
      type: String, // Will store the file path or URL of the uploaded image
      required: true,
    }
  }
);

module.exports = mongoose.model("Event", EventSchema);
